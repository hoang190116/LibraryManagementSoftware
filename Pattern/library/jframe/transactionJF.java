    public void setValue(){
        boxBook.removeAllItems();
        list = bookDAO.findBook();
        for (books object : list) {
            boxBook.addItem(String.valueOf(object.getBook_id()));
        }
        
        boxReader.removeAllItems();
        list2 = readerDAO.findRead();
        for (readers object : list2) {
            boxReader.addItem(String.valueOf(object.getReaderID()));
        }
    }
    public void shown(){
        list3 = transactionDAO.findTrans();
        tb.setRowCount(0);
        for (transactions object : list3) {
            tb.addRow(new Object[]{object.getTransactionID(), object.getReaderID(), object.getBookID()});
        }
    }
