    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int select = transactionTb.getSelectedRow();
       if(select>=0){
           transactions tr = list3.get(select);
           int option = JOptionPane.showConfirmDialog(this, "Delete?");
           if(option == 0){
               transactionDAO.delete(tr.getTransactionID());
           }
       }
       shown();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        AbstractFactory fac = AbstractFactory.getFactory(Types.transaction);
        transaction tr = (transaction) fac.getTransaction(Types.transaction);
        tr.builder(new transactionBuilder().readerID(Integer.parseInt(boxReader.getSelectedItem().toString()))
        .bookID(Integer.parseInt(boxBook.getSelectedItem().toString())));
        transactionDAO.insert(tr);
        shown();
    }//GEN-LAST:event_jButton1ActionPerformed
