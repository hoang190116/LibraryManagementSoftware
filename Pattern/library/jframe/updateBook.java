    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try{
        AbstractFactory fac = AbstractFactory.getFactory(Types.book);
        book b = (book) fac.getBook(Types.book);
        b.builder(new bookBuilder().book_id(id).book_name(name.getText()).author(author.getText())
        .publish(pub.getText()).type(type.getText()).area(area.getText()));
        bookDAO.update(b);
        JOptionPane.showMessageDialog(this, "Done");
        
        this.setVisible(false);
        librarianJF li = new librarianJF();
        li.setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Exception Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

