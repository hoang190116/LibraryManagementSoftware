    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String uname = name.getText();
        String pas = pass.getText();
        AbstractFactory fac = AbstractFactory.getFactory(Types.librarian);
        librarian li = (librarian) fac.getLibrarian(Types.librarian);
        li.builder(new librarianBuilder().name(uname).pass(pas));
        if(login.loginCheck(li) == 1){
            this.setVisible(false);
            librarianJF lib = new librarianJF();
            lib.setVisible(true);
        }
        else if(login.loginCheck(li) == 2){
            this.setVisible(false);
            readerView v = new readerView();
            v.setVisible(true);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Wrong Username or Password!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

