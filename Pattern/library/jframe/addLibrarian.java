    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        AbstractFactory fac = AbstractFactory.getFactory(Types.librarian);
        librarian li = (librarian) fac.getLibrarian(Types.librarian);
        if(name.getText().length() >0 && name.getText() != null && pass.getText().length() >0 && pass.getText() != null)
        {
            li.builder(new librarianBuilder().name(name.getText()).pass(pass.getText()));
            librarianDAO.insert(li);
            shown();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

